% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

g11 = str2mat(get(handles.edit1,'string'));
for i=1:length(g11)
    g1(i)=str2num(g11(i));
end
g22 = str2mat(get(handles.edit2,'string'));
for i=1:length(g22)
    g2(i)=str2num(g22(i));
end
g33 = str2mat(get(handles.edit3,'string'));
for i=1:length(g33)
    g3(i)=str2num(g33(i));
end
mes = str2mat(get(handles.edit4,'string'));
for i=1:length(mes)
    M(i)=str2num(mes(i));
end

x1 = mod(conv(M,g1),2);
x2 = mod(conv(M,g2),2);
x3 = mod(conv(M,g3),2);

a = length(x3);
code = zeros(1,2*a);
j = 1;

for i=1:a
    code(j)=x1(i);j=j+1;
    code(j)=x2(i);j=j+1;
    code(j)=x3(i);j=j+1;
end
set(handles.edit5,'string',num2str(code));

